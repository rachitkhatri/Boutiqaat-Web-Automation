# config/__init__.py
#
# PURPOSE:
#   This file marks the 'config' directory as a Python package.
#
# WHAT DOES IT DO?
#   By having this file here, Python recognizes 'config' as a
#   package, which allows us to import modules from it.
#
# EXAMPLE USAGE:
#   from config.settings import BASE_DOMAIN
#   from config.settings import DEFAULT_TIMEOUT
#
# WHY IS IT EMPTY?
#   We don't need any initialization code for the config package.
#   We only need this file to exist so Python treats 'config'
#   as a package rather than just a regular folder.
#
# WHAT IF THIS FILE DIDN'T EXIST?
#   Without this file, you would get an error:
#   "ModuleNotFoundError: No module named 'config'"
#
# PYTHON VERSION NOTE:
#   - Python 2.x: This file is REQUIRED
#   - Python 3.3+: This file is optional but RECOMMENDED
#   - Best Practice: Always include it for clarity
#
